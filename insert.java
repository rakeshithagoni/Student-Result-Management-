    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno=jTextField1.getText();
        String physics=jTextField2.getText();
        String chemistry=jTextField3.getText();
        String maths=jTextField4.getText();
        String dbms=jTextField5.getText();
        String os=jTextField6.getText();
        
         try {
            // TODO add your handling code here:
            Class.forName("com.mysql.cj.jdbc.Driver");
              Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Subhadra@5");
              Statement st=con.createStatement();
              ResultSet rs=st.executeQuery("select * from student where rollno='"+rollno+"'");
              if(rs.next()){
                  st.executeUpdate("insert into result(rollno,physics,chemistry,maths,dbms,os) values('"+rollno+"','"+physics+"','"+chemistry+"','"+maths+"','"+dbms+"','"+os+"')");  
                   JOptionPane.showMessageDialog(null, "succesfully updated the result");
                   setVisible(false);
                   new insert().setVisible(true);
              }else{
                   JOptionPane.showMessageDialog(null, "rollno does not exist");
              }
              
        } catch(Exception e){
             JOptionPane.showMessageDialog(null, e.toString());
        }
         
        
    }//GEN-LAST:event_jButton6ActionPerformed

